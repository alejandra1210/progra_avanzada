module.exports = {

    areaTriangulo: function(a, b) {

        return (a + b) / 2;

    },

    areaCuadrado: function(a, b) {

        return a * b;

    },

    areaCirculo: function(a) {

        return 3, 14 * (a * a);

    }
}

